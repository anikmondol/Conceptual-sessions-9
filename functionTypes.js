// no parameters, but return
// parameters, no return
// parameters, return





// no parameters, no return

function fun1(){
    console.log('no parameters, no return');
}





// no parameters, but return


function fun2(){
    return 'no parameters, but return'
}




// parameters, no return

function fun3(name){
    console.log('my name is ', name);
}



// parameters, return

function fun4(x, y){
    return x + y;
}